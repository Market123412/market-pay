function seededValue(seed: number, min: number, max: number): number {
  const x = Math.sin(seed * 9301 + 49297) * 49297;
  const rand = x - Math.floor(x);
  return min + rand * (max - min);
}

function generateReviews(seed: number, categorySlug: string): ProductReview[] {
  const comments = reviewComments[categorySlug] || reviewComments.eletronicos;
  const count = 1 + Math.floor(seededValue(seed, 0, 3));
  const reviews: ProductReview[] = [];
  for (let i = 0; i < count; i++) {
    const authorIdx = Math.floor(seededValue(seed + i * 7, 0, reviewAuthors.length));
    const commentIdx = Math.floor(seededValue(seed + i * 13, 0, comments.length));
    const rating = Math.floor(seededValue(seed + i * 3, 4, 5.99));
    reviews.push({
      author: reviewAuthors[authorIdx],
      rating,
      comment: comments[commentIdx],
      date: `2025-0${1 + Math.floor(seededValue(seed + i, 0, 3))}-${10 + Math.floor(seededValue(seed + i * 2, 0, 18))}`,
    });
  }
  return reviews;
}

export function generateAllProducts(startId: number): Product[] {
  const generated: Product[] = [];
  let id = startId;

  for (const template of templates) {
    for (let i = 0; i < template.names.length; i++) {
      const name = template.names[i];
      const seed = id * 31 + i * 17;

      const sourceIdx = i % sources.length;
      const source = sources[sourceIdx];
      const imageIdx = i % template.images.length;
      const image = template.images[imageIdx];
      const sellerIdx = i % template.sellers.length;
      const featuresIdx = i % template.features.length;

      const rawPrice = seededValue(seed, template.priceRange[0], template.priceRange[1]);
      const price = Math.round(rawPrice * 100) / 100;
      const discountPct = Math.round(seededValue(seed + 1, template.discountRange[0], template.discountRange[1]));
      const hasDiscount = seededValue(seed + 2, 0, 1) > 0.3;
      const discount = hasDiscount ? discountPct : undefined;
      const originalPrice = discount ? Math.round(price / (1 - discount / 100) * 100) / 100 : undefined;

      const rating = Math.round(seededValue(seed + 3, 3.8, 5.0) * 10) / 10;
      const reviewCount = Math.round(seededValue(seed + 4, 50, 8000));
      const freeShipping = seededValue(seed + 5, 0, 1) > 0.35;

      generated.push({
        id: String(id),
        title: name,
        description: template.descriptionTemplate(name),
        price,
        originalPrice,
        discount,
        image,
        images: [image.replace("w=500", "w=800")],
        category: template.category,
        categorySlug: template.categorySlug,
        source,
        affiliateUrl: buildAffiliateUrl(source, name),
        rating: Math.min(rating, 5.0),
        reviewCount,
        reviews: generateReviews(seed, template.categorySlug),
        freeShipping,
        seller: template.sellers[sellerIdx],
        features: template.features[featuresIdx],
      });

      id++;
    }
  }

  return generated;
}
